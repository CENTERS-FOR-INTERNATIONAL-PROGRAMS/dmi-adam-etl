    type_of_care::text AS type_of_care,
    health_facility_name::text AS health_facility_name,
    health_facility_mfl_number::text AS health_facility_mfl_number,
    date_of_admission::text AS date_of_admission,
    inpatient_number::text AS inpatient_number,
    outpatient_number::text AS outpatient_number,
    outcome_of_patient::text AS outcome_of_patient,
    date_of_death::text AS date_of_death,
    status_of_patient::text AS status_of_patient,
    date_of_discharge::text AS date_of_discharge,
    were_laboratory_samples_collected::text AS were_laboratory_samples_collected,
    laboratory_samples_collected::text AS laboratory_samples_collected,
    date_of_sample_collection::text AS date_of_sample_collection,
    laboratory_test_conducted::text AS laboratory_test_conducted,
    laboratory_test_conducted_other::text AS laboratory_test_conducted_other,
    date_of_laboratory_testing::text AS date_of_laboratory_testing,
    date_of_laboratory_testing_results::text AS date_of_laboratory_testing_results,
    result_of_laboratory_test::text AS result_of_laboratory_test,
    laboratory_facility_name::text AS laboratory_facility_name,
    laboratory_facility_name_other::text AS laboratory_facility_name_other
FROM {{ ref('stg_respiratory_syndrome') }}
